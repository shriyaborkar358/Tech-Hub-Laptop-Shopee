let toggleLike = true;

function like1() {
    const likeButton = document.getElementById("like1")
    if (toggleLike) {
        likeButton.src = "./../images/laptop-store-img/red-like.png";

        toggleLike = false;
    }
    else {
        likeButton.src = "./../images/laptop-store-img/blank-like.png";

        toggleLike = true;
    }
}

